function rotl(x: number, n: number): number {
    return ((x << n) | (x >>> (32 - n))) >>> 0;
}

function tau(a: number): number {
    return (
        ((SBOX[(a >>> 24) & 0xff] << 24) |
            (SBOX[(a >>> 16) & 0xff] << 16) |
            (SBOX[(a >>> 8) & 0xff] << 8) |
            SBOX[a & 0xff]) >>>
        0
    );
}

function transformL(b: number): number {
    return (b ^ rotl(b, 2) ^ rotl(b, 10) ^ rotl(b, 18) ^ rotl(b, 24)) >>> 0;
}

function transformLp(b: number): number {
    return (b ^ rotl(b, 13) ^ rotl(b, 23)) >>> 0;
}

function u32be(buf: Buffer, off: number): number {
    return buf.readUInt32BE(off);
}

function keySchedule(key16: Buffer): number[] {
    const K = [u32be(key16, 0) ^ FK[0], u32be(key16, 4) ^ FK[1], u32be(key16, 8) ^ FK[2], u32be(key16, 12) ^ FK[3]].map(
        v => v >>> 0,
    );
    const rk: number[] = [];
    for (let i = 0; i < 32; i++) {
        const t = (K[1] ^ K[2] ^ K[3] ^ CK[i]) >>> 0;
        const b = transformLp(tau(t));
        const next = (K[0] ^ b) >>> 0;
        K[0] = K[1];
        K[1] = K[2];
        K[2] = K[3];
        K[3] = next;
        rk.push(next);
    }
    return rk;
}

function encryptBlock(rk: number[], block: Buffer): Buffer {
    const X = [u32be(block, 0), u32be(block, 4), u32be(block, 8), u32be(block, 12)];
    for (let i = 0; i < 32; i++) {
        const t = (X[1] ^ X[2] ^ X[3] ^ rk[i]) >>> 0;
        const next = (X[0] ^ transformL(tau(t))) >>> 0;
        X[0] = X[1];
        X[1] = X[2];
        X[2] = X[3];
        X[3] = next;
    }
    const out = Buffer.alloc(16);
    out.writeUInt32BE(X[3] >>> 0, 0);
    out.writeUInt32BE(X[2] >>> 0, 4);
    out.writeUInt32BE(X[1] >>> 0, 8);
    out.writeUInt32BE(X[0] >>> 0, 12);
    return out;
}

/**
 * Raw single-block encrypt (16 bytes in, 16 bytes out) — used by the GM/T self-test.
 *
 * @param key16
 * @param block16
 */
export function sm4EncryptBlock(key16: Buffer, block16: Buffer): Buffer {
    return encryptBlock(keySchedule(key16), block16);
}

/**
 * SM4-ECB encrypt with PKCS#7 padding.
 *
 * @param data
 * @param key
 */
export function sm4EcbEncryptPkcs7(data: Buffer, key: Buffer = SM4_KEY): Buffer {
    const rk = keySchedule(key);
    const pad = 16 - (data.length % 16);
    const padded = Buffer.concat([data, Buffer.alloc(pad, pad)]);
    const out = Buffer.alloc(padded.length);
    for (let i = 0; i < padded.length; i += 16) {
        encryptBlock(rk, padded.subarray(i, i + 16)).copy(out, i);
    }
    return out;
}

export type CodeTransform = 'plain' | 'padRight32' | 'padLeft32';

/**
 * base64( SM4_ECB_PKCS7( transform(code) ) ). Mirrors `omoda_auth.sm4_code`.
 * - padRight32 = left-justify to width 32 (pad spaces on the right) — only if shorter.
 * - padLeft32  = right-justify to width 32 (pad spaces on the left) — only if shorter.
 *
 * @param code
 * @param transform
 */
export function sm4Code(code: string, transform: CodeTransform = 'plain'): string {
    let s = String(code);
    if (transform === 'padRight32' && s.length < 32) {
        s = s.padEnd(32, ' ');
    } else if (transform === 'padLeft32' && s.length < 32) {
        s = s.padStart(32, ' ');
    }
    return sm4EcbEncryptPkcs7(Buffer.from(s, 'utf-8')).toString('base64');
}
